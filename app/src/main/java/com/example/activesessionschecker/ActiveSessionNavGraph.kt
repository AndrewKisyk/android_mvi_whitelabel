import androidx.navigation.compose.rememberNavController
import com.example.activesessionschecker.ui.screens.sessions.SessionsScreen
import kotlinx.coroutines.CoroutineScope
import kotlinx.coroutines.launch

@Composable
fun ActiveSessionNavGraph(
    modifier: Modifier = Modifier,
    navController: NavHostController = rememberNavController(),
    coroutineScope: CoroutineScope = rememberCoroutineScope(),
    drawerState: DrawerState = rememberDrawerState(initialValue = DrawerValue.Closed),
    startDestination: String = TodoDestinations.SESSIONS_ROUTE,
    navActions: ActiveSessionNavigation = remember(navController) {
        ActiveSessionNavigation(navController)
    }
) {
    val currentNavBackStackEntry by navController.currentBackStackEntryAsState()
    val currentRoute = currentNavBackStackEntry?.destination?.route ?: startDestination

    NavHost(
        navController = navController,
        startDestination = startDestination,
        modifier = modifier
    ) {
        composable(
            TodoDestinations.SESSIONS_ROUTE,
            arguments = listOf()
        ) { entry ->
            SessionsScreen(
                userMessage = R.string.app_name,
                onUserMessageDisplayed = {  },
                onAddTask = { },
                onSessionClick = { session -> navActions.navigateToSessionDetail(session.id) },
                openDrawer = { coroutineScope.launch { drawerState.open() } }
            )
        }
        composable(TodoDestinations.SESSION_DETAIL_ROUTE) {

        }
    }
}

// Keys for navigation
const val ADD_EDIT_RESULT_OK = Activity.RESULT_FIRST_USER + 1
const val DELETE_RESULT_OK = Activity.RESULT_FIRST_USER + 2
const val EDIT_RESULT_OK = Activity.RESULT_FIRST_USER + 3
